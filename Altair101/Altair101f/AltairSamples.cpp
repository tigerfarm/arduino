void loadKillTheBitOriginal() {
  int theSize = sizeof(KillTheBitOriginal);
  int theLineCounter = 1;
  Serial.println(F("+ Octal listing,"));
  for (int i = 0; i < theSize; i++ ) {
    byte theValue = pgm_read_byte(&KillTheBitOriginal[i]);
    MWRITE(i, theValue);
    Serial.print(F(" 0x"));
    printOctal(theValue);
    if (theLineCounter > 16) {
      Serial.println();
      theLineCounter = 0;
    }
    theLineCounter++;
  }
}

